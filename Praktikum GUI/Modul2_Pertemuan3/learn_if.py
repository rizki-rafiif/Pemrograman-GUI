# mempelajari if - else dalam bahasa python
x = False
y = True

# if else
if x != True :
    print('Jadi')
else :
    print('Gak Jadi')


# if elif
if x != False :
    print('Jadi 2')
elif y != False :
    print('Gak Jadi 2')

# if dengan and kemudian else
if x != True and y == False :
    print('Jadi 3')
else :
    print('Gak jadi 3')
